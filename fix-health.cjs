function b(ch){ const n=ch.codePointAt(0); return cp1252[n] ?? (n <= 255 ? n : null); }
function fixChunk(m){
  const bytes=[];
  for (const ch of [...m]) {
    const x=b(ch);
    if (x === null) return m;
    bytes.push(x);
  }
  const r=Buffer.from(bytes).toString("utf8");
  return r.includes("�") ? m : r;
}

let s = fs.readFileSync(p,"utf8");
s = s.replace(/[\u0080-\u00ff\u0152\u0153\u0160\u0161\u0178\u017d\u017e\u2018-\u201d\u2020-\u2026\u2030\u2039\u203a\u20ac]+/g, fixChunk);
